ログインの履歴を残すテーブルを作る場合は以下のように全て名前が決まります。

テーブル名

login_records
複数形、スネークケース



モデル名

LoginRecord
単数形、アッパーキャメルケース



コントローラ


LoginRecordsController
複数形、アッパーキャメル



ここ重要です
※※特にテーブル名とモデル名は規則に沿って名前をつけないと紐づきません。




/*****************************************
 * 複数の単語を組み合わせる場合
 * プログラミングにおいて複数キーワードの名前の付け方
 * アッパーキャメル記法、
 * ローワーキャメル記法
 * スネーク記法
 * 記述方法があります。
 *
 *****************************************/


/*
|--------------------------------------------------------------------------
| アッパーキャメル記法（※パスカルケースとも呼びます。）
|--------------------------------------------------------------------------
|
| [例] user + parameter ⇒ UserParameter
|
| 先頭を大文字で書き始め、複合語のスペースをなくし要素の先頭を大文字にします。
| ※文字のラインが凸凹になっており、ラクダのこぶのように見えることからキャメル記法と名付けられました。
|
*/



/*****************************************
 * Laravelでの使用方法
 *
 *****************************************/




モデルで使用する場合	単数形が原則


UserMsg.php
AccessRanking.php


コントローラで使用する場合
単数形・複数形どちらでもOK

UserMsgController.php
AccessRankingController.php

/*
|--------------------------------------------------------------------------
| ローワーキャメル記法
|--------------------------------------------------------------------------
|
| [例] user + parameter ⇒ userParameter
|
| 先頭を小文字で書き始め、複合語のスペースをなくし要素の先頭は大文字にします。
| ローワーキャメルを単にキャメルケースと読んだりします。
|
*/



/*****************************************
 * Laravelでの使用方法
 * メソッドで使用する
 *****************************************/


見本
userProfile()
postRegister()

/*
|--------------------------------------------------------------------------
| スネーク記法
|--------------------------------------------------------------------------
| [例] user + parameter ⇒ user_parameter
|
| 単語と単語の区切りにアンダースコアを使う記述方法をスネーク記法と呼びます。
| 文字のラインがくねくねした蛇に見えることからスネーク記法と名づけられました。
|
| Laravelではメソッドの名前やマイグレーションファイルの名前、DBのカラム名を付けるときに使用します。
*/



/*****************************************
 * Laravelでの使用方法
 * 変数名
 *****************************************/


$user_name
$college_name_current

/*****************************************
 * Laravelでの使用方法
 * Bladeのファイル名
 *****************************************/

user_admin.blade.php
user_profile.blade.php


/*****************************************
 * Laravelでの使用方法
 * マイグレーションのファイル名
 *****************************************/

create_user_table.php
add_pref_to_users.php








